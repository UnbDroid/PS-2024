def charada_1():
  """
  Esta função apresenta a charada 1 e verifica a resposta do usuário.
  """
  
  print("Charada 1:")
  print("Eu sou um número inteiro.")
  print("Se você me dividir por 2, o meu quociente é um número inteiro e o meu resto é nulo.")
  print("Além disso, se você olhar para Pi, eu estarei lá.")
  print("Que número eu sou?")

  resposta = input("Resposta: ")

  if resposta == "4":
    print("Parabéns! Você acertou!")
  else:
    print("Resposta incorreta. O número correto é 4.")



